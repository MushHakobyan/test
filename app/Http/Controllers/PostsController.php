     */
    public function store(Request $request)
    {
        $request->validate([
            'title' => 'required|unique:posts|max:255',
            'message' => 'required',
        ]);


        $posts =new Post;
        $posts->title = $request->input('title');
        $posts->users_id = Auth::id();
        $posts->message = $request->input('message');
        $posts->save();

        return redirect('home');
    }

